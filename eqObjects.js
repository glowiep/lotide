const eqArrays = function(arr1, arr2) {
  if (arr1.length === arr2.length) {
    for (let i = 0; i < arr1.length; i++) {
      let perfectMatch = false;
      if (arr1[i] === arr2[i]) {
        perfectMatch = true;
      } else {
        perfectMatch = false;
        break;
      }
      return perfectMatch;
    }
  }
};

